package org.example.timerserver.Model;
/**
 * Интерфейс Субъекта (Subject) для паттерна "Наблюдатель" (Observer Pattern).
 *
 * <p>Определяет контракт для классов, которые могут выступать в роли издателя
 * событий. Субъект поддерживает список наблюдателей и уведомляет их об изменениях
 * своего состояния.</p>
 *
 * <p>Реализация этого интерфейса позволяет:</p>
 * <ul>
 *   <li>Подписывать наблюдателей на события ({@link #attach(Observer)})</li>
 *   <li>Отписывать наблюдателей ({@link #detach(Observer)})</li>
 *   <li>Уведомлять всех подписчиков ({@link #notifyAllObservers()})</li>
 *   <li>Управлять состоянием субъекта ({@link #getState()}, {@link #setState(int)})</li>
 * </ul>
 *
 * <p><b>Пример использования:</b></p>
 * <pre>
 * public class PulseServer implements Subject {
 *     private List&lt;Observer&gt; observers = new ArrayList&lt;&gt;();
 *     private int state = 0;
 *
 *     {@literal @}Override
 *     public void attach(Observer obs) {
 *         observers.add(obs);
 *     }
 *
 *     {@literal @}Override
 *     public void setState(int time) {
 *         this.state = time;
 *         notifyAllObservers(); // Автоматическое уведомление
 *     }
 * }
 * </pre>
 *
 * @author Дмитрий
 * @version 1.0
 * @since 2026-04-24
 * @see Observer
 * @see PulseServer
 */

public interface Subject {

    /**
     * Уведомляет всех подписанных наблюдателей об изменении состояния.
     *
     * <p>Обычно вызывается автоматически внутри методов {@link #setState(int)}
     * или при наступлении определенных событий. Каждый наблюдатель получает
     * уведомление через свой метод {@link Observer#update(String)}.</p>
     *
     * <p><b>Примечание:</b> Реализация должна обрабатывать возможные исключения
     * в методах наблюдателей, чтобы сбой одного не нарушил уведомление остальных.</p>
     */

    void notifyAllObservers(); // уведомить всех наблюдателей

    /**
     * Добавляет нового наблюдателя в список подписчиков.
     *
     * <p>После вызова этого метода, добавленный наблюдатель будет получать
     * все последующие уведомления от субъекта. Наблюдатель должен реализовывать
     * интерфейс {@link Observer}.</p>
     *
     * <p><b>Пример:</b></p>
     * <pre>
     * TemperatureSensor sensor = new TemperatureSensor();
     * pulseServer.attach(sensor); // Датчик начнет получать уведомления
     * </pre>
     *
     * @param obs объект, реализующий интерфейс Observer (не может быть null)
     * @throws NullPointerException если observer = null
     */

    void attach(Observer obs); // добавить наблюдателя

    /**
     * Удаляет наблюдателя из списка подписчиков.
     *
     * <p>После удаления наблюдатель перестает получать уведомления.
     * Если указанный наблюдатель не был подписан, метод ничего не делает
     * (без генерации исключения).</p>
     *
     * <p><b>Рекомендация:</b> Используйте этот метод для освобождения ресурсов
     * при завершении работы компонентов системы.</p>
     *
     * @param obs объект, реализующий интерфейс Observer
     */

    void detach(Observer obs); // удалить наблюдателя

    /**
     * Возвращает текущее состояние субъекта.
     *
     * <p>Состояние может быть любым целочисленным значением, отражающим
     * текущий статус субъекта. В контексте Pulse Server - это текущее
     * время в секундах с момента запуска системы.</p>
     *
     * @return текущее значение состояния субъекта
     */

    int getState(); // получить текущее состояние

    /**
     * Устанавливает новое состояние субъекта.
     *
     * <p>При изменении состояния обычно автоматически вызывается метод
     * {@link #notifyAllObservers()} для уведомления всех подписчиков
     * об изменениях. Это ключевой механизм паттерна "Наблюдатель".</p>
     *
     * <p><b>Важно:</b> Реализация должна гарантировать потокобезопасность
     * при многопоточном доступе, так как состояние может изменяться из
     * разных потоков (например


     , из таймера).</p>
     *
     * @param time новое значение состояния (обычно время в секундах)
     */

    void setState(int time); // установить состояние
}
